class User {
  final String Uid;
  final String Name;
  final String Gender;
  final String Weight;
  final String Age;
  final String Reg;
  final String Email;
  final String Phone;
  final String Token;
  final String Image;

  const User({
    required this.Uid,
    required this.Name,
    required this.Gender,
    required this.Weight,
    required this.Age,
    required this.Reg,
    required this.Email,
    required this.Phone,
    required this.Token,
    required this.Image,
  });
}